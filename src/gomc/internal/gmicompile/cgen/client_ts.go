// GenerateClientTS writes a TypeScript REST client module for calling the API
// over HTTP/JSON. Uses the Fetch API (browser/Node 18+).
func GenerateClientTS(w io.Writer, api *ast.API) error {
	g := &clientTSGen{w: w, api: api}
	return g.generate()
}

type clientTSGen struct {
	w   io.Writer
	api *ast.API
	err error
}

func (g *clientTSGen) printf(format string, args ...interface{}) {
	if g.err != nil {
		return
	}
	_, g.err = fmt.Fprintf(g.w, format, args...)
}

func (g *clientTSGen) generate() error {
	g.emitHeader()
	g.emitConstants()
	g.emitEnums()
	g.emitInterfaces()
	g.emitClient()
	g.emitClientMethods()
	g.printf("}\n")
	return g.err
}

// --- Header ---

func (g *clientTSGen) emitHeader() {
	g.printf("// Code generated by gmicompile --client-ts - DO NOT EDIT.\n")
	EmitMeta(g.w, g.api, "//")
	g.printf("//\n")
	g.printf("// REST client for %s API v%d\n\n", g.api.Name, g.api.Version)
}

// --- Constants ---

func (g *clientTSGen) emitConstants() {
	if len(g.api.Consts) == 0 {
		return
	}
	g.printf("// --- Constants ---\n\n")
	for _, c := range g.api.Consts {
		g.printf("export const %s = %d;\n", c.Name, c.Value)
	}
	g.printf("\n")
}

// --- Enums ---

func (g *clientTSGen) emitEnums() {
	if len(g.api.Enums) == 0 {
		return
	}
	g.printf("// --- Enums ---\n\n")
	for _, e := range g.api.Enums {
		name := toPascalCase(e.Name)
		g.printf("export enum %s {\n", name)
		for _, v := range e.Values {
			g.printf("  %s = %d,\n", v.Name, v.Value)
		}
		g.printf("}\n\n")
	}
}

// --- Interfaces (types) ---

func (g *clientTSGen) emitInterfaces() {
	if len(g.api.Types) == 0 {
		return
	}
	g.printf("// --- Types ---\n\n")
	for _, t := range g.api.Types {
		name := toPascalCase(t.Name)
		g.printf("export interface %s {\n", name)
		for _, f := range t.Fields {
			tsType := g.toTSType(f.Type)
			if f.Type.Nullable {
				g.printf("  %s?: %s;\n", f.Name, tsType)
			} else {
				g.printf("  %s: %s;\n", f.Name, tsType)
			}
		}
		g.printf("}\n\n")
	}
}

// --- Client ---

func (g *clientTSGen) emitClient() {
	className := toPascalCase(g.api.Name) + "Client"

	g.printf("// --- Client ---\n\n")

	// APIError class
	g.printf("export class APIError extends Error {\n")
	g.printf("  constructor(public readonly statusCode: number, message: string) {\n")
	g.printf("    super(`${message} (HTTP ${statusCode})`);\n")
	g.printf("    this.name = 'APIError';\n")
	g.printf("  }\n")
	g.printf("}\n\n")

	// Client class
	g.printf("export class %s {\n", className)
	g.printf("  private readonly baseUrl: string;\n\n")

	// Constructor
	g.printf("  constructor(baseUrl: string, instance: string = '%s') {\n", g.api.Prefix)
	g.printf("    this.baseUrl = baseUrl.replace(/\\/+$/, '') + '/api/v1/' + instance;\n")
	g.printf("  }\n\n")

	// _doRequest helper
	g.printf("  private async doRequest<T>(method: string, path: string, body?: unknown): Promise<T | undefined> {\n")
	g.printf("    const url = this.baseUrl + path;\n")
	g.printf("    const init: RequestInit = { method };\n")
	g.printf("    if (body !== undefined) {\n")
	g.printf("      init.headers = { 'Content-Type': 'application/json', 'Accept': 'application/json' };\n")
	g.printf("      init.body = JSON.stringify(body);\n")
	g.printf("    } else {\n")
	g.printf("      init.headers = { 'Accept': 'application/json' };\n")
	g.printf("    }\n")
	g.printf("    const resp = await fetch(url, init);\n")
	g.printf("    if (!resp.ok) {\n")
	g.printf("      let msg: string;\n")
	g.printf("      try {\n")
	g.printf("        const errData = await resp.json();\n")
	g.printf("        msg = errData.error ?? resp.statusText;\n")
	g.printf("      } catch {\n")
	g.printf("        msg = await resp.text();\n")
	g.printf("      }\n")
	g.printf("      throw new APIError(resp.status, msg);\n")
	g.printf("    }\n")
	g.printf("    const text = await resp.text();\n")
	g.printf("    if (text) {\n")
	g.printf("      return JSON.parse(text) as T;\n")
	g.printf("    }\n")
	g.printf("    return undefined;\n")
	g.printf("  }\n\n")
}

// --- Client Methods ---

func (g *clientTSGen) emitClientMethods() {
	if len(g.api.Funcs) == 0 {
		return
	}
	g.printf("  // --- API Methods ---\n\n")

	for _, fn := range g.api.Funcs {
		if fn.Watch {
			continue // watch functions use WebSocket, not REST
		}
		g.emitClientMethod(fn)
	}
}

func (g *clientTSGen) emitClientMethod(fn ast.Func) {
	methodName := toCamelCaseTS(fn.Name)

	// Signature
	params := g.methodParams(fn)
	retType := g.methodReturnType(fn)
	g.printf("  async %s(%s): Promise<%s> {\n", methodName, params, retType)

	// Build path
	path := fn.Path
	pathParams := extractPathParams(path)

	if len(pathParams) > 0 {
		// Use template literal for path interpolation
		tsPath := path
		for _, pp := range pathParams {
			paramName := toCamelCaseTS(pp)
			tsPath = strings.Replace(tsPath, "{"+pp+"}", "${encodeURIComponent(String("+paramName+"))}", 1)
		}
		g.printf("    const apiPath = `%s`;\n", tsPath)
	} else {
		g.printf("    const apiPath = '%s';\n", path)
	}

	// Query parameters (GET/DELETE)
	queryParams := g.nonPathParams(fn, pathParams)
	if len(queryParams) > 0 && (fn.Method == "GET" || fn.Method == "DELETE" || fn.Method == "") {
		g.printf("    const query = new URLSearchParams();\n")
		for _, qp := range queryParams {
			paramName := toCamelCaseTS(qp.Name)
			if qp.Type.Nullable {
				g.printf("    if (%s !== undefined) query.set('%s', String(%s));\n",
					paramName, qp.Name, paramName)
			} else {
				g.printf("    query.set('%s', String(%s));\n", qp.Name, paramName)
			}
		}
		g.printf("    const qs = query.toString();\n")
		g.printf("    const fullPath = qs ? `${apiPath}?${qs}` : apiPath;\n")
	}

	// Body (POST/PUT/PATCH)
	var bodyExpr string
	hasQuery := len(queryParams) > 0 && (fn.Method == "GET" || fn.Method == "DELETE" || fn.Method == "")
	if len(queryParams) > 0 && (fn.Method == "POST" || fn.Method == "PUT" || fn.Method == "PATCH") {
		g.printf("    const body = {\n")
		for _, bp := range queryParams {
			paramName := toCamelCaseTS(bp.Name)
			g.printf("      %s: %s,\n", bp.Name, paramName)
		}
		g.printf("    };\n")
		bodyExpr = "body"
	}

	// HTTP method
	method := fn.Method
	if method == "" {
		method = "GET"
	}

	// Path variable to use
	pathVar := "apiPath"
	if hasQuery {
		pathVar = "fullPath"
	}

	// Make request
	hasResult := fn.Return != nil
	if !hasResult {
		if bodyExpr != "" {
			g.printf("    await this.doRequest<void>('%s', %s, %s);\n", method, pathVar, bodyExpr)
		} else {
			g.printf("    await this.doRequest<void>('%s', %s);\n", method, pathVar)
		}
	} else {
		tsType := g.toTSType(*fn.Return)
		if bodyExpr != "" {
			g.printf("    return await this.doRequest<%s>('%s', %s, %s) as %s;\n", tsType, method, pathVar, bodyExpr, tsType)
		} else {
			g.printf("    return await this.doRequest<%s>('%s', %s) as %s;\n", tsType, method, pathVar, tsType)
		}
	}
	g.printf("  }\n\n")
}

func (g *clientTSGen) methodParams(fn ast.Func) string {
	if len(fn.Params) == 0 {
		return ""
	}
	var parts []string
	// Required params first, optional last
	for _, p := range fn.Params {
		if !p.Type.Nullable {
			parts = append(parts, fmt.Sprintf("%s: %s", toCamelCaseTS(p.Name), g.toTSType(p.Type)))
		}
	}
	for _, p := range fn.Params {
		if p.Type.Nullable {
			parts = append(parts, fmt.Sprintf("%s?: %s", toCamelCaseTS(p.Name), g.toTSType(p.Type)))
		}
	}
	return strings.Join(parts, ", ")
}

func (g *clientTSGen) methodReturnType(fn ast.Func) string {
	if fn.Return == nil {
		return "void"
	}
	return g.toTSType(*fn.Return)
}

func (g *clientTSGen) nonPathParams(fn ast.Func, pathParams []string) []ast.Param {
	pathSet := make(map[string]bool)
	for _, pp := range pathParams {
		pathSet[pp] = true
	}
	var result []ast.Param
	for _, p := range fn.Params {
		if !pathSet[p.Name] {
			result = append(result, p)
		}
	}
	return result
}

func (g *clientTSGen) toTSType(t ast.TypeRef) string {
	switch t.Kind {
	case ast.TypePrimitive:
		base := primitiveToTSType(t.Name)
		if t.Nullable {
			return base + " | undefined"
		}
		return base
	case ast.TypeNamed:
		name := toPascalCase(t.Name)
		if t.Nullable {
			return name + " | undefined"
		}
		return name
	case ast.TypeSlice:
		elem := g.toTSType(*t.Elem)
		return elem + "[]"
	case ast.TypeArray:
		elem := g.toTSType(*t.Elem)
		return elem + "[]"
	}
	return "unknown"
}

func primitiveToTSType(name string) string {
	switch name {
	case "bool":
		return "boolean"
	case "i8", "u8", "i32", "u32", "f32", "f64":
		return "number"
	case "i64", "u64":
		return "number" // JSON doesn't support bigint natively
	case "string":
		return "string"
	default:
		return "unknown"
	}
}

// toCamelCaseTS converts snake_case to camelCase and escapes JS reserved words.
func toCamelCaseTS(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if i > 0 && len(p) > 0 {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	result := strings.Join(parts, "")
	return tsEscapeReserved(result)
}

// tsEscapeReserved adds a trailing underscore to JavaScript/TypeScript reserved words.
func tsEscapeReserved(name string) string {
	switch name {
	case "break", "case", "catch", "class", "const", "continue", "debugger",
		"default", "delete", "do", "else", "enum", "export", "extends",
		"false", "finally", "for", "function", "if", "import", "in",
		"instanceof", "new", "null", "return", "super", "switch", "this",
		"throw", "true", "try", "typeof", "var", "void", "while", "with",
		"yield", "let", "static", "implements", "interface", "package",
		"private", "protected", "public", "await", "async":
		return name + "_"
	}
	return name
}
