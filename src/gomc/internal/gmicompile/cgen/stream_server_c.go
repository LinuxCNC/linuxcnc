package cgen

import (
	"fmt"
	"io"
	"strings"

	"github.com/sittner/linuxcnc/src/gomc/internal/gmicompile/ast"
)

// GenerateStreamServerHeader writes a C header with callback typedefs and
// a callbacks struct for each stream_server block in the API.
// Returns false if the API has no stream_server blocks.
func GenerateStreamServerHeader(w io.Writer, api *ast.API) (bool, error) {
	if len(api.StreamServers) == 0 {
		return false, nil
	}
	g := &streamServerCGen{w: w, api: api}
	return true, g.generate()
}

type streamServerCGen struct {
	w   io.Writer
	api *ast.API
	err error
}

func (g *streamServerCGen) printf(format string, args ...interface{}) {
	if g.err != nil {
		return
	}
	_, g.err = fmt.Fprintf(g.w, format, args...)
}

func (g *streamServerCGen) generate() error {
	guard := strings.ToUpper(g.api.Name) + "_STREAM_API_H"

	g.printf("// Generated by gmicompile - DO NOT EDIT\n")
	g.printf("#ifndef %s\n", guard)
	g.printf("#define %s\n\n", guard)
	g.printf("#include <stdint.h>\n")
	g.printf("#include <stddef.h>\n\n")
	g.printf("#ifdef __cplusplus\nextern \"C\" {\n#endif\n\n")

	for _, ss := range g.api.StreamServers {
		g.emitStreamServer(ss)
	}

	g.printf("#ifdef __cplusplus\n}\n#endif\n\n")
	g.printf("#endif // %s\n", guard)
	return g.err
}

func (g *streamServerCGen) emitStreamServer(ss ast.StreamServer) {
	prefix := ss.Name

	g.printf("// --- stream_server: %s ---\n\n", ss.Name)

	// Emit callback typedefs
	for _, fn := range ss.Funcs {
		g.emitCallbackTypedef(prefix, fn)
	}

	// Emit callbacks struct
	g.printf("typedef struct {\n")
	g.printf("    void *ctx;\n")
	g.printf("    char *cfg;  // config string sent to client on connect (e.g. \"fff\")\n")
	for _, fn := range ss.Funcs {
		g.printf("    %s_%s_fn %s;\n", prefix, fn.Name, fn.Name)
	}
	g.printf("} %s_stream_callbacks_t;\n\n", prefix)

	// Emit registration helper (static inline using gomc_api_t)
	g.printf("// Register stream server callbacks via gomc_api_t.\n")
	g.printf("#ifndef %s_API_CGO\n", strings.ToUpper(g.api.Name))
	g.printf("#include \"gomc_api.h\"\n")
	g.printf("static inline int %s_stream_register(\n", prefix)
	g.printf("    gomc_api_t *api, const char *instance_name,\n")
	g.printf("    const %s_stream_callbacks_t *cb) {\n", prefix)
	g.printf("    return api->register_api(api->ctx, \"%s_stream\", 1, instance_name, cb);\n", prefix)
	g.printf("}\n")
	g.printf("#endif\n\n")
}

func (g *streamServerCGen) emitCallbackTypedef(prefix string, fn ast.StreamFunc) {
	// Return type
	retType := "void"
	if fn.Return != nil {
		retType = streamCType(fn.Return)
	}

	// Build parameter list
	params := "void *ctx"
	for _, p := range fn.Params {
		params += ", " + streamCType(&p.Type) + " " + p.Name
	}

	g.printf("typedef %s (*%s_%s_fn)(%s);\n", retType, prefix, fn.Name, params)
}

// streamCType converts a TypeRef to a C type string for stream_server headers.
func streamCType(t *ast.TypeRef) string {
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case "bool":
			return "int"
		case "i8":
			return "int8_t"
		case "u8":
			return "uint8_t"
		case "i32":
			return "int32_t"
		case "u32":
			return "uint32_t"
		case "i64":
			return "int64_t"
		case "u64":
			return "uint64_t"
		case "f32":
			return "float"
		case "f64":
			return "double"
		case "ptr":
			return "void *"
		case "string":
			return "const char *"
		}
	}
	return "void *"
}
