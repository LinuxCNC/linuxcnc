func TestGenerateClientPythonSimple(t *testing.T) {
	api := &ast.API{
		Name:       "testapi",
		Version:    1,
		Prefix:     "testapi",
		RestExport: true,
		Consts: []ast.Const{
			{Name: "MAX_ITEMS", Value: 100},
		},
		Enums: []ast.Enum{
			{
				Name: "Color",
				Values: []ast.EnumValue{
					{Name: "RED", Value: 0},
					{Name: "GREEN", Value: 1},
					{Name: "BLUE", Value: 2},
				},
			},
		},
		Types: []ast.Type{
			{
				Name: "Item",
				Fields: []ast.Field{
					{Name: "name", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
					{Name: "value", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "f64"}},
					{Name: "color", Type: ast.TypeRef{Kind: ast.TypeNamed, Name: "Color"}},
					{Name: "opt_desc", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string", Nullable: true}},
				},
			},
		},
		Funcs: []ast.Func{
			{
				Name:   "list_items",
				Method: "GET",
				Path:   "/items",
				Params: []ast.Param{
					{Name: "pattern", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string", Nullable: true}},
				},
				Return: &ast.TypeRef{Kind: ast.TypeSlice, Elem: &ast.TypeRef{Kind: ast.TypeNamed, Name: "Item"}},
			},
			{
				Name:   "get_item",
				Method: "GET",
				Path:   "/item/{name}",
				Params: []ast.Param{
					{Name: "name", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
				},
				Return: &ast.TypeRef{Kind: ast.TypeNamed, Name: "Item"},
			},
			{
				Name:   "create_item",
				Method: "POST",
				Path:   "/item",
				Params: []ast.Param{
					{Name: "name", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
					{Name: "value", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "f64"}},
				},
				Return: &ast.TypeRef{Kind: ast.TypeNamed, Name: "Item"},
			},
			{
				Name:   "delete_item",
				Method: "DELETE",
				Path:   "/item/{name}",
				Params: []ast.Param{
					{Name: "name", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
				},
			},
		},
	}

	var buf bytes.Buffer
	err := GenerateClientPython(&buf, api)
	if err != nil {
		t.Fatalf("GenerateClientPython: %v", err)
	}

	out := buf.String()

	// Check header
	assertContains(t, out, "# Code generated by gmicompile --client-python - DO NOT EDIT.")
	assertContains(t, out, "from __future__ import annotations")
	assertContains(t, out, "import json")
	assertContains(t, out, "import urllib.request")
	assertContains(t, out, "import urllib.parse")
	assertContains(t, out, "from dataclasses import dataclass")
	assertContains(t, out, "from enum import IntEnum")

	// Check constant
	assertContains(t, out, "MAX_ITEMS = 100")

	// Check enum
	assertContains(t, out, "class Color(IntEnum):")
	assertContains(t, out, "    RED = 0")
	assertContains(t, out, "    GREEN = 1")
	assertContains(t, out, "    BLUE = 2")

	// Check dataclass
	assertContains(t, out, "@dataclass")
	assertContains(t, out, "class Item:")
	assertContains(t, out, "    name: str")
	assertContains(t, out, "    value: float")
	assertContains(t, out, "    color: Color")
	assertContains(t, out, "    opt_desc: Optional[str] = None")

	// Check from_dict
	assertContains(t, out, "def from_dict(cls, d: dict) -> Item:")

	// Check to_dict
	assertContains(t, out, "def to_dict(self) -> dict:")

	// Check APIError
	assertContains(t, out, "class APIError(Exception):")
	assertContains(t, out, "self.status_code = status_code")
	assertContains(t, out, "self.message = message")

	// Check client class
	assertContains(t, out, "class TestapiClient:")
	assertContains(t, out, `"/api/v1/testapi"`)

	// Check _do_request
	assertContains(t, out, "def _do_request(self, method: str, path: str,")

	// Check method signatures
	assertContains(t, out, "def list_items(self, pattern: Optional[str] = None) -> list[Item]:")
	assertContains(t, out, "def get_item(self, name: str) -> Item:")
	assertContains(t, out, "def create_item(self, name: str, value: float) -> Item:")
	assertContains(t, out, "def delete_item(self, name: str) -> None:")

	// Check path parameter substitution
	assertContains(t, out, `path = path.replace("{name}", urllib.parse.quote(str(name), safe=""))`)

	// Check query param handling for nullable
	assertContains(t, out, "if pattern is not None:")
	assertContains(t, out, `query["pattern"] = str(pattern)`)

	// Check POST body
	assertContains(t, out, `"name": name,`)
	assertContains(t, out, `"value": value,`)

	// Check HTTP methods used
	assertContains(t, out, `self._do_request("GET", path,`)
	assertContains(t, out, `self._do_request("POST", path,`)
	assertContains(t, out, `self._do_request("DELETE", path,`)

	// Check return type conversion
	assertContains(t, out, "Item.from_dict(item) for item in")
	assertContains(t, out, "Item.from_dict(result) if result else None")
}

func TestGenerateClientPythonPathParams(t *testing.T) {
	api := &ast.API{
		Name:       "paths",
		Version:    1,
		Prefix:     "paths",
		RestExport: true,
		Funcs: []ast.Func{
			{
				Name:   "get_nested",
				Method: "GET",
				Path:   "/parent/{parentId}/child/{childId}",
				Params: []ast.Param{
					{Name: "parentId", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
					{Name: "childId", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"}},
				},
				Return: &ast.TypeRef{Kind: ast.TypePrimitive, Name: "string"},
			},
		},
	}

	var buf bytes.Buffer
	err := GenerateClientPython(&buf, api)
	if err != nil {
		t.Fatalf("GenerateClientPython: %v", err)
	}

	out := buf.String()

	assertContains(t, out, `path = path.replace("{parentId}", urllib.parse.quote(str(parent_id), safe=""))`)
	assertContains(t, out, `path = path.replace("{childId}", urllib.parse.quote(str(child_id), safe=""))`)
}

func TestGenerateClientPythonPrimitiveReturn(t *testing.T) {
	api := &ast.API{
		Name:       "simple",
		Version:    1,
		Prefix:     "simple",
		RestExport: true,
		Funcs: []ast.Func{
			{
				Name:   "get_count",
				Method: "GET",
				Path:   "/count",
				Return: &ast.TypeRef{Kind: ast.TypePrimitive, Name: "i32"},
			},
			{
				Name:   "set_value",
				Method: "POST",
				Path:   "/value",
				Params: []ast.Param{
					{Name: "value", Type: ast.TypeRef{Kind: ast.TypePrimitive, Name: "f64"}},
				},
			},
		},
	}

	var buf bytes.Buffer
	err := GenerateClientPython(&buf, api)
	if err != nil {
		t.Fatalf("GenerateClientPython: %v", err)
	}

	out := buf.String()

	assertContains(t, out, "def get_count(self) -> int:")
	assertContains(t, out, "return result")
	assertContains(t, out, "def set_value(self, value: float) -> None:")
}
