// GenerateClientPython writes a Python REST client module for calling the API
// over HTTP/JSON. Uses urllib.request (stdlib only, no external dependencies).
func GenerateClientPython(w io.Writer, api *ast.API) error {
	g := &clientPyGen{w: w, api: api}
	return g.generate()
}

type clientPyGen struct {
	w   io.Writer
	api *ast.API
	err error
}

func (g *clientPyGen) printf(format string, args ...interface{}) {
	if g.err != nil {
		return
	}
	_, g.err = fmt.Fprintf(g.w, format, args...)
}

func (g *clientPyGen) generate() error {
	g.emitHeader()
	g.emitConstants()
	g.emitEnums()
	g.emitTypes()
	g.emitClient()
	g.emitClientMethods()
	return g.err
}

// --- Header ---

func (g *clientPyGen) emitHeader() {
	g.printf("# Code generated by gmicompile --client-python - DO NOT EDIT.\n")
	EmitMeta(g.w, g.api, "#")
	g.printf("#\n")
	g.printf("# REST client for %s API v%d\n\n", g.api.Name, g.api.Version)
	g.printf("from __future__ import annotations\n\n")
	g.printf("import json\n")
	g.printf("import urllib.request\n")
	g.printf("import urllib.parse\n")
	g.printf("import urllib.error\n")
	g.printf("from dataclasses import dataclass, field\n")
	g.printf("from enum import IntEnum\n")
	g.printf("from typing import Any, Optional\n\n\n")
}

// --- Constants ---

func (g *clientPyGen) emitConstants() {
	if len(g.api.Consts) == 0 {
		return
	}
	g.printf("# --- Constants ---\n\n")
	for _, c := range g.api.Consts {
		g.printf("%s = %d\n", c.Name, c.Value)
	}
	g.printf("\n\n")
}

// --- Enums ---

func (g *clientPyGen) emitEnums() {
	if len(g.api.Enums) == 0 {
		return
	}
	g.printf("# --- Enums ---\n\n")
	for _, e := range g.api.Enums {
		className := toPascalCase(e.Name)
		g.printf("class %s(IntEnum):\n", className)
		for _, v := range e.Values {
			g.printf("    %s = %d\n", v.Name, v.Value)
		}
		g.printf("\n\n")
	}
}

// --- Types ---

func (g *clientPyGen) emitTypes() {
	if len(g.api.Types) == 0 {
		return
	}
	g.printf("# --- Types ---\n\n")
	for _, t := range g.api.Types {
		className := toPascalCase(t.Name)
		g.printf("@dataclass\n")
		g.printf("class %s:\n", className)
		for _, f := range t.Fields {
			fieldName := f.Name
			pyType := g.toPyType(f.Type)
			if f.Type.Nullable {
				g.printf("    %s: %s = None\n", fieldName, pyType)
			} else {
				g.printf("    %s: %s = %s\n", fieldName, pyType, g.pyDefault(f.Type))
			}
		}
		g.printf("\n")

		// from_dict class method
		g.printf("    @classmethod\n")
		g.printf("    def from_dict(cls, d: dict) -> %s:\n", className)
		g.printf("        return cls(\n")
		for i, f := range t.Fields {
			fieldName := f.Name
			comma := ","
			if i == len(t.Fields)-1 {
				comma = ","
			}
			g.printf("            %s=d.get(%q, %s)%s\n", fieldName, fieldName, g.pyDefault(f.Type), comma)
		}
		g.printf("        )\n")

		// to_dict method
		g.printf("\n")
		g.printf("    def to_dict(self) -> dict:\n")
		g.printf("        return {\n")
		for _, f := range t.Fields {
			fieldName := f.Name
			g.printf("            %q: self.%s,\n", fieldName, fieldName)
		}
		g.printf("        }\n")
		g.printf("\n\n")
	}
}

// --- Client ---

func (g *clientPyGen) emitClient() {
	className := toPascalCase(g.api.Name) + "Client"

	g.printf("# --- Client ---\n\n")

	// APIError
	g.printf("class APIError(Exception):\n")
	g.printf("    \"\"\"Error response from the server.\"\"\"\n\n")
	g.printf("    def __init__(self, status_code: int, message: str):\n")
	g.printf("        self.status_code = status_code\n")
	g.printf("        self.message = message\n")
	g.printf("        super().__init__(f\"{message} (HTTP {status_code})\")\n")
	g.printf("\n\n")

	// ValidationError — raised client-side (collect-all) before a request.
	g.printf("class ValidationError(Exception):\n")
	g.printf("    \"\"\"One or more inputs violated an IDL @constraint.\"\"\"\n\n")
	g.printf("    def __init__(self, errors: list):\n")
	g.printf("        self.errors = errors\n")
	g.printf("        super().__init__(\"validation failed: \" + \"; \".join(errors))\n")
	g.printf("\n\n")

	// Client class
	g.printf("class %s:\n", className)
	g.printf("    \"\"\"REST client for the %s API.\"\"\"\n\n", g.api.Name)

	// Constructor
	g.printf("    def __init__(self, base_url: str, instance: str = %q):\n", g.api.Name)
	g.printf("        self.base_url = base_url.rstrip(\"/\") + \"/api/v1/\" + instance\n\n")

	// _do_request helper
	g.printf("    def _do_request(self, method: str, path: str, body: Any = None, has_result: bool = False) -> Any:\n")
	g.printf("        url = self.base_url + path\n")
	g.printf("        data = None\n")
	g.printf("        if body is not None:\n")
	g.printf("            data = json.dumps(body).encode(\"utf-8\")\n")
	g.printf("        req = urllib.request.Request(url, data=data, method=method)\n")
	g.printf("        req.add_header(\"Accept\", \"application/json\")\n")
	g.printf("        if data is not None:\n")
	g.printf("            req.add_header(\"Content-Type\", \"application/json\")\n")
	g.printf("        try:\n")
	g.printf("            with urllib.request.urlopen(req) as resp:\n")
	g.printf("                resp_body = resp.read()\n")
	g.printf("                if has_result and resp_body:\n")
	g.printf("                    return json.loads(resp_body)\n")
	g.printf("                return None\n")
	g.printf("        except urllib.error.HTTPError as e:\n")
	g.printf("            err_body = e.read().decode(\"utf-8\", errors=\"replace\")\n")
	g.printf("            try:\n")
	g.printf("                err_data = json.loads(err_body)\n")
	g.printf("                msg = err_data.get(\"error\", err_body)\n")
	g.printf("            except (json.JSONDecodeError, ValueError):\n")
	g.printf("                msg = err_body\n")
	g.printf("            raise APIError(e.code, msg)\n")
	g.printf("\n")
}

// --- Client Methods ---

func (g *clientPyGen) emitClientMethods() {
	if len(g.api.Funcs) == 0 {
		return
	}
	g.printf("    # --- API Methods ---\n\n")

	for _, fn := range g.api.Funcs {
		g.emitClientMethod(fn)
	}
}

func (g *clientPyGen) emitClientMethod(fn ast.Func) {
	methodName := toSnakeCase(fn.Name)

	// Signature
	params := g.methodParams(fn)
	retType := g.methodReturnAnnotation(fn)
	g.printf("    def %s(self%s) -> %s:\n", methodName, params, retType)

	// Docstring
	if fn.Doc != "" {
		g.printf("        \"\"\"%s\"\"\"\n", fn.Doc)
	}

	// Client-side collect-all validation of IDL @constraints.
	if v := clientValidation(pyLang{}, fn, g.api); v != "" {
		g.printf("%s", spaceIndent(v, 4, 4))
	}

	// Build path
	path := fn.Path
	pathParams := extractPathParams(path)

	if len(pathParams) > 0 {
		g.printf("        path = %q\n", path)
		for _, pp := range pathParams {
			paramName := toSnakeCase(pp)
			g.printf("        path = path.replace(\"{%s}\", urllib.parse.quote(str(%s), safe=\"\"))\n", pp, paramName)
		}
	} else {
		g.printf("        path = %q\n", path)
	}

	// Query parameters (GET/DELETE)
	queryParams := g.nonPathParams(fn, pathParams)
	if len(queryParams) > 0 && (fn.Method == "GET" || fn.Method == "DELETE" || fn.Method == "") {
		g.printf("        query = {}\n")
		for _, qp := range queryParams {
			paramName := toSnakeCase(qp.Name)
			if qp.Type.Nullable {
				g.printf("        if %s is not None:\n", paramName)
				g.printf("            query[%q] = str(%s)\n", toSnakeCase(qp.Name), paramName)
			} else {
				g.printf("        query[%q] = str(%s)\n", toSnakeCase(qp.Name), paramName)
			}
		}
		g.printf("        if query:\n")
		g.printf("            path += \"?\" + urllib.parse.urlencode(query)\n")
	}

	// Body (POST/PUT/PATCH)
	var bodyExpr string
	if len(queryParams) > 0 && (fn.Method == "POST" || fn.Method == "PUT" || fn.Method == "PATCH") {
		g.printf("        body = {\n")
		for _, bp := range queryParams {
			paramName := toSnakeCase(bp.Name)
			g.printf("            %q: %s,\n", toSnakeCase(bp.Name), paramName)
		}
		g.printf("        }\n")
		bodyExpr = "body"
	} else {
		bodyExpr = "None"
	}

	// HTTP method
	method := fn.Method
	if method == "" {
		method = "GET"
	}

	// Make request
	hasResult := fn.Return != nil
	if !hasResult {
		g.printf("        self._do_request(%q, path, %s)\n", method, bodyExpr)
	} else {
		g.printf("        result = self._do_request(%q, path, %s, has_result=True)\n", method, bodyExpr)
		// Convert result to typed object if it's a named type or slice of named
		if fn.Return.Kind == ast.TypeSlice && fn.Return.Elem != nil && fn.Return.Elem.Kind == ast.TypeNamed {
			elemClass := toPascalCase(fn.Return.Elem.Name)
			g.printf("        return [%s.from_dict(item) for item in (result or [])]\n", elemClass)
		} else if fn.Return.Kind == ast.TypeNamed {
			retClass := toPascalCase(fn.Return.Name)
			g.printf("        return %s.from_dict(result) if result else None\n", retClass)
		} else {
			g.printf("        return result\n")
		}
	}
	g.printf("\n")
}

func (g *clientPyGen) methodParams(fn ast.Func) string {
	if len(fn.Params) == 0 {
		return ""
	}
	var parts []string
	// Put required params first, optional (nullable) last
	for _, p := range fn.Params {
		if !p.Type.Nullable {
			parts = append(parts, fmt.Sprintf("%s: %s", p.Name, g.toPyType(p.Type)))
		}
	}
	for _, p := range fn.Params {
		if p.Type.Nullable {
			parts = append(parts, fmt.Sprintf("%s: %s = None", p.Name, g.toPyType(p.Type)))
		}
	}
	return ", " + strings.Join(parts, ", ")
}

func (g *clientPyGen) methodReturnAnnotation(fn ast.Func) string {
	if fn.Return == nil {
		return "None"
	}
	return g.toPyType(*fn.Return)
}

func (g *clientPyGen) nonPathParams(fn ast.Func, pathParams []string) []ast.Param {
	pathSet := make(map[string]bool)
	for _, pp := range pathParams {
		pathSet[pp] = true
	}
	var result []ast.Param
	for _, p := range fn.Params {
		if !pathSet[p.Name] {
			result = append(result, p)
		}
	}
	return result
}

func (g *clientPyGen) toPyType(t ast.TypeRef) string {
	switch t.Kind {
	case ast.TypePrimitive:
		base := primitiveToPyType(t.Name)
		if t.Nullable {
			return "Optional[" + base + "]"
		}
		return base
	case ast.TypeNamed:
		name := toPascalCase(t.Name)
		if t.Nullable {
			return "Optional[" + name + "]"
		}
		return name
	case ast.TypeSlice:
		elem := g.toPyType(*t.Elem)
		return "list[" + elem + "]"
	case ast.TypeArray:
		elem := g.toPyType(*t.Elem)
		return "list[" + elem + "]"
	}
	return "Any"
}

func (g *clientPyGen) pyDefault(t ast.TypeRef) string {
	if t.Nullable {
		return "None"
	}
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case "bool":
			return "False"
		case "string":
			return `""`
		default:
			return "0"
		}
	case ast.TypeNamed:
		return "None"
	case ast.TypeSlice, ast.TypeArray:
		return "field(default_factory=list)"
	}
	return "None"
}

func primitiveToPyType(name string) string {
	switch name {
	case "bool":
		return "bool"
	case "i8", "u8", "i32", "u32", "i64", "u64":
		return "int"
	case "f32", "f64":
		return "float"
	case "string":
		return "str"
	default:
		return "Any"
	}
}
