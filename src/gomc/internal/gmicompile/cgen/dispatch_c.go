// GenerateDispatchC writes a Go file with cgo that bridges C callbacks
// to the apiserver. This file:
//   - Includes the C header via cgo preamble
//   - Generates static C call wrappers for function pointers
//   - Generates Go types matching the C types
//   - Generates CToGo struct converters
//   - Generates DispatchFunc wrappers for REST
//   - Generates APIMeta with embedded dispatch
//   - Generates //export register function callable from C
func GenerateDispatchC(w io.Writer, api *ast.API, packageName, headerFile string) error {
	g := &dispatchCGen{w: w, api: api, pkg: packageName, header: headerFile}
	return g.generate()
}

type dispatchCGen struct {
	w      io.Writer
	api    *ast.API
	pkg    string
	header string // C header filename, e.g. "hal_api.h"
	err    error
	tmpSeq int // counter for unique temp variable names
	ce     *constraintEmitter
}

func (g *dispatchCGen) printf(format string, args ...interface{}) {
	if g.err != nil {
		return
	}
	_, g.err = fmt.Fprintf(g.w, format, args...)
}

func (g *dispatchCGen) generate() error {
	g.ce = newConstraintEmitter(g.api, "Re")
	g.emitCgoPreamble()
	g.emitImports()
	g.emitConstants()
	g.emitGoTypes()
	g.emitConverters()
	g.emitGoToCConverters()
	g.printf("%s", g.ce.regexVarDecls())
	g.emitDispatchFuncs()
	g.emitMeta()
	g.emitRegister()
	return g.err
}

// --- cgo Preamble ---

func (g *dispatchCGen) emitCgoPreamble() {
	g.printf("// Code generated by gmicompile - DO NOT EDIT.\n")
	EmitMeta(g.w, g.api, "//")
	g.printf("\n")
	g.printf("package %s\n\n", g.pkg)

	// Add cgo CFLAGS for imported APIs so their headers can be found
	if len(g.api.Imports) > 0 {
		var flags []string
		for _, imp := range g.api.Imports {
			flags = append(flags, fmt.Sprintf("-I../%s", imp.Name))
		}
		g.printf("// #cgo CFLAGS: %s\n", strings.Join(flags, " "))
	}

	g.printf("/*\n")
	g.printf("#define %s_API_CGO\n", strings.ToUpper(g.api.Name))
	// Also define CGO guards for imported APIs to skip their gomc_api.h sections
	for _, imp := range g.api.Imports {
		g.printf("#define %s_API_CGO\n", strings.ToUpper(imp.Name))
	}
	g.printf("#include \"%s\"\n", g.header)
	g.printf("#include <stdlib.h>\n")
	g.printf("#include <string.h>\n")
	g.printf("\n")

	// Static call wrappers — cgo cannot call C function pointers directly,
	// so we generate a static wrapper for each callback.
	for _, fn := range g.api.Funcs {
		if fn.Publish {
			continue // publish functions use ring buffers, not callbacks
		}
		g.emitCallWrapper(fn)
	}

	g.printf("*/\n")
	g.printf("import \"C\"\n\n")
}

func (g *dispatchCGen) emitCallWrapper(fn ast.Func) {
	apiName := g.api.Name
	fnSnake := toSnakeCase(fn.Name)
	wrapperName := fmt.Sprintf("call_%s_%s", apiName, fnSnake)
	fnType := fmt.Sprintf("%s_%s_fn", apiName, fnSnake)

	// Determine return type: direct return or void
	retCType := "void"
	if fn.Return != nil {
		if fn.Return.Kind == ast.TypeSlice {
			retCType = fmt.Sprintf("%s_%s_result_t", apiName, fnSnake)
		} else {
			retCType = toCTypeForAPI(apiName, *fn.Return)
		}
	}

	// Build parameter list: fn pointer + ctx + same params as callback typedef
	params := []string{fmt.Sprintf("%s _fn_ptr", fnType), "void *ctx"}
	args := []string{"ctx"}

	for _, p := range fn.Params {
		name := p.Name
		params = append(params, g.cgoParamDecl(apiName, p))
		args = append(args, name)
		// Slice params also pass a length arg
		if p.Type.Kind == ast.TypeSlice {
			args = append(args, name+"_len")
		}
	}

	g.printf("static %s %s(%s) {\n", retCType, wrapperName, strings.Join(params, ", "))
	if fn.Return != nil {
		g.printf("    return _fn_ptr(%s);\n", strings.Join(args, ", "))
	} else {
		g.printf("    _fn_ptr(%s);\n", strings.Join(args, ", "))
	}
	g.printf("}\n\n")
}

// cgoParamDecl returns the C parameter declaration for use in cgo preamble wrappers.
func (g *dispatchCGen) cgoParamDecl(apiName string, p ast.Param) string {
	name := p.Name

	// ptr qualifier: pass-through typed pointer, no marshaling.
	if p.IsPtr {
		switch p.Type.Kind {
		case ast.TypePrimitive:
			return fmt.Sprintf("%s *%s", primitiveToCType(p.Type.Name), name)
		default:
			cType := fmt.Sprintf("%s_%s_t", apiName, toSnakeCase(p.Type.Name))
			return fmt.Sprintf("%s *%s", cType, name)
		}
	}

	switch p.Type.Kind {
	case ast.TypePrimitive:
		cType := primitiveToCType(p.Type.Name)
		if p.ByRef || p.IsOut {
			return fmt.Sprintf("%s *%s", cType, name)
		}
		return fmt.Sprintf("%s %s", cType, name)

	case ast.TypeNamed:
		cType := fmt.Sprintf("%s_%s_t", apiName, toSnakeCase(p.Type.Name))
		if p.ByRef || p.IsOut {
			return fmt.Sprintf("%s *%s", cType, name)
		}
		if g.isEnum(p.Type.Name) {
			return fmt.Sprintf("%s %s", cType, name)
		}
		return fmt.Sprintf("const %s *%s", cType, name)

	case ast.TypeSlice:
		elemCType := toCTypeForAPI(apiName, *p.Type.Elem)
		if p.ByRef || p.IsOut {
			return fmt.Sprintf("%s *%s, size_t %s_len", elemCType, name, name)
		}
		return fmt.Sprintf("const %s *%s, size_t %s_len", elemCType, name, name)

	case ast.TypeArray:
		elemCType := toCTypeForAPI(apiName, *p.Type.Elem)
		sizeStr := cgoArraySizeStr(apiName, p.Type)
		if p.ByRef || p.IsOut {
			return fmt.Sprintf("%s %s[%s]", elemCType, name, sizeStr)
		}
		return fmt.Sprintf("const %s %s[%s]", elemCType, name, sizeStr)
	}

	return fmt.Sprintf("void *%s", name)
}

// cgoArraySizeStr returns the C size expression for an array type (using #define name).
func cgoArraySizeStr(apiName string, t ast.TypeRef) string {
	if t.ArrayLenName != "" {
		return fmt.Sprintf("%s_%s", strings.ToUpper(apiName), t.ArrayLenName)
	}
	return fmt.Sprintf("%d", t.ArrayLen)
}

// --- Go Imports ---

func (g *dispatchCGen) emitImports() {
	g.printf("import (\n")
	g.printf("\t\"encoding/json\"\n")
	g.printf("\t\"fmt\"\n")
	g.printf("\t\"syscall\"\n")
	g.printf("\t\"unsafe\"\n")
	g.printf("\n")
	g.printf("\t\"github.com/sittner/linuxcnc/src/gomc/internal/apiserver\"\n")
	g.printf(")\n\n")

	// Suppress unused import warnings
	g.printf("var _ = json.Marshal\n")
	g.printf("var _ = fmt.Sprintf\n")
	g.printf("var _ = syscall.EINVAL\n")
	g.printf("var _ unsafe.Pointer\n\n")
}

// --- Go Constants ---

func (g *dispatchCGen) emitConstants() {
	if len(g.api.Consts) == 0 {
		return
	}
	g.printf("// --- Constants ---\n\n")
	g.printf("const (\n")
	for _, c := range g.api.Consts {
		g.printf("\t%s = %d\n", c.Name, c.Value)
	}
	g.printf(")\n\n")
}

// --- Go Types (same as server_go, needed in the cgo package) ---

func (g *dispatchCGen) emitGoTypes() {
	// Enums
	if len(g.api.Enums) > 0 {
		g.printf("// --- Enums ---\n\n")
		for _, e := range g.api.Enums {
			goName := toPascalCase(e.Name)
			g.printf("type %s int32\n\n", goName)
			g.printf("const (\n")
			for _, v := range e.Values {
				g.printf("\t%s_%s %s = %d\n", goName, v.Name, goName, v.Value)
			}
			g.printf(")\n\n")
		}
	}

	// Structs
	if len(g.api.Types) > 0 {
		g.printf("// --- Types ---\n\n")
		for _, t := range g.api.Types {
			goName := toPascalCase(t.Name)
			g.printf("type %s struct {\n", goName)
			for _, f := range t.Fields {
				fieldName := toPascalCase(f.Name)
				fieldType := goTypeForDispatch(f.Type)
				jsonTag := f.Name
				omit := ""
				if f.Type.Nullable {
					omit = ",omitempty"
				}
				g.printf("\t%s %s `json:\"%s%s\"`\n", fieldName, fieldType, jsonTag, omit)
			}
			g.printf("}\n\n")
		}
	}
}

// --- CToGo Converters ---

func (g *dispatchCGen) emitConverters() {
	if len(g.api.Types) == 0 {
		return
	}
	g.printf("// --- CToGo Converters ---\n\n")

	for _, t := range g.api.Types {
		goName := toPascalCase(t.Name)
		cType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Name))
		funcName := toLowerCamelRaw(t.Name) + "CToGo"

		g.printf("func %s(src *%s) %s {\n", funcName, cType, goName)
		g.printf("\treturn %s{\n", goName)
		for _, f := range t.Fields {
			goField := toPascalCase(f.Name)
			cField := cgoFieldAccess(f.Name)
			g.emitFieldCToGo(goField, "src."+cField, f.Type)
		}
		g.printf("\t}\n")
		g.printf("}\n\n")
	}

	// Generate xxxCToGoFree wrappers for types that contain slice fields.
	// These call the no-free base converter, then free the C-allocated slices.
	for _, t := range g.api.Types {
		sliceFields := g.sliceFields(t)
		if len(sliceFields) == 0 {
			continue
		}
		goName := toPascalCase(t.Name)
		cType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Name))
		baseName := toLowerCamelRaw(t.Name) + "CToGo"
		freeName := baseName + "Free"

		g.printf("func %s(src *%s) %s {\n", freeName, cType, goName)
		g.printf("\tresult := %s(src)\n", baseName)
		for _, f := range sliceFields {
			cField := "src." + cgoFieldAccess(f.Name)
			g.printf("\tif %s != nil { C.free(unsafe.Pointer(%s)) }\n", cField, cField)
		}
		g.printf("\treturn result\n")
		g.printf("}\n\n")
	}
}

// sliceFields returns all fields of a type that have TypeSlice kind.
func (g *dispatchCGen) sliceFields(t ast.Type) []ast.Field {
	var result []ast.Field
	for _, f := range t.Fields {
		if f.Type.Kind == ast.TypeSlice {
			result = append(result, f)
		}
	}
	return result
}

// typeHasSlices checks if a named type has any slice fields.
func (g *dispatchCGen) typeHasSlices(name string) bool {
	for _, t := range g.api.Types {
		if t.Name == name {
			return len(g.sliceFields(t)) > 0
		}
	}
	return false
}

func (g *dispatchCGen) emitFieldCToGo(goField, cExpr string, t ast.TypeRef) {
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case ast.PrimString:
			if t.Nullable {
				g.printf("\t\t%s: func() string { if %s != nil { return C.GoString(%s) }; return \"\" }(),\n", goField, cExpr, cExpr)
			} else {
				g.printf("\t\t%s: C.GoString(%s),\n", goField, cExpr)
			}
		case ast.PrimBool:
			if t.Nullable {
				g.printf("\t\t%s: func() *bool { v := bool(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: bool(%s),\n", goField, cExpr)
			}
		case ast.PrimI8:
			g.printf("\t\t%s: int8(%s),\n", goField, cExpr)
		case ast.PrimU8:
			g.printf("\t\t%s: uint8(%s),\n", goField, cExpr)
		case ast.PrimI16:
			if t.Nullable {
				g.printf("\t\t%s: func() *int16 { v := int16(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: int16(%s),\n", goField, cExpr)
			}
		case ast.PrimU16:
			if t.Nullable {
				g.printf("\t\t%s: func() *uint16 { v := uint16(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: uint16(%s),\n", goField, cExpr)
			}
		case ast.PrimI32:
			if t.Nullable {
				g.printf("\t\t%s: func() *int32 { v := int32(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: int32(%s),\n", goField, cExpr)
			}
		case ast.PrimU32:
			if t.Nullable {
				g.printf("\t\t%s: func() *uint32 { v := uint32(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: uint32(%s),\n", goField, cExpr)
			}
		case ast.PrimI64:
			if t.Nullable {
				g.printf("\t\t%s: func() *int64 { v := int64(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: int64(%s),\n", goField, cExpr)
			}
		case ast.PrimU64:
			if t.Nullable {
				g.printf("\t\t%s: func() *uint64 { v := uint64(%s); return &v }(),\n", goField, cExpr)
			} else {
				g.printf("\t\t%s: uint64(%s),\n", goField, cExpr)
			}
		case ast.PrimF32:
			g.printf("\t\t%s: float32(%s),\n", goField, cExpr)
		case ast.PrimF64:
			g.printf("\t\t%s: float64(%s),\n", goField, cExpr)
		}
	case ast.TypeNamed:
		// Could be enum or struct — for enums, cast; for structs, recurse
		goType := toPascalCase(t.Name)
		if g.isEnum(t.Name) {
			g.printf("\t\t%s: %s(%s),\n", goField, goType, cExpr)
		} else {
			converter := toLowerCamelRaw(t.Name) + "CToGo"
			if t.Nullable {
				g.printf("\t\t%s: func() *%s { v := %s(&%s); return &v }(),\n", goField, goType, converter, cExpr)
			} else {
				g.printf("\t\t%s: %s(&%s),\n", goField, converter, cExpr)
			}
		}
	case ast.TypeSlice:
		// Slice fields in C structs: pointer 'field' + companion 'field_len'.
		// Convert to Go slice, freeing the C array after copy.
		lenExpr := cExpr + "_len"
		if t.Elem != nil {
			switch t.Elem.Kind {
			case ast.TypePrimitive:
				if t.Elem.Name == ast.PrimString {
					// []string: char** + len → []string
					g.printf("\t\t%s: func() []string {\n", goField)
					g.printf("\t\t\tn := int(%s)\n", lenExpr)
					g.printf("\t\t\tif n == 0 || %s == nil { return nil }\n", cExpr)
					g.printf("\t\t\tcSlice := unsafe.Slice(%s, n)\n", cExpr)
					g.printf("\t\t\tresult := make([]string, n)\n")
					g.printf("\t\t\tfor i := 0; i < n; i++ { result[i] = C.GoString(cSlice[i]) }\n")
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				} else if t.Elem.Name == ast.PrimU8 {
					// []u8: uint8_t* + len → []byte (binary data)
					g.printf("\t\t%s: func() []byte {\n", goField)
					g.printf("\t\t\tn := int(%s)\n", lenExpr)
					g.printf("\t\t\tif n == 0 || %s == nil { return nil }\n", cExpr)
					g.printf("\t\t\tresult := C.GoBytes(unsafe.Pointer(%s), C.int(n))\n", cExpr)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				} else {
					// Other primitive slices: cast pointer, copy elements
					goElem := goTypeForDispatch(*t.Elem)
					g.printf("\t\t%s: func() []%s {\n", goField, goElem)
					g.printf("\t\t\tn := int(%s)\n", lenExpr)
					g.printf("\t\t\tif n == 0 || %s == nil { return nil }\n", cExpr)
					g.printf("\t\t\tcSlice := unsafe.Slice(%s, n)\n", cExpr)
					g.printf("\t\t\tresult := make([]%s, n)\n", goElem)
					g.printf("\t\t\tfor i := 0; i < n; i++ { result[i] = %s(cSlice[i]) }\n", goElem)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				}
			case ast.TypeNamed:
				if g.isEnum(t.Elem.Name) {
					goElem := toPascalCase(t.Elem.Name)
					g.printf("\t\t%s: func() []%s {\n", goField, goElem)
					g.printf("\t\t\tn := int(%s)\n", lenExpr)
					g.printf("\t\t\tif n == 0 || %s == nil { return nil }\n", cExpr)
					g.printf("\t\t\tcSlice := unsafe.Slice(%s, n)\n", cExpr)
					g.printf("\t\t\tresult := make([]%s, n)\n", goElem)
					g.printf("\t\t\tfor i := 0; i < n; i++ { result[i] = %s(cSlice[i]) }\n", goElem)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				} else {
					// []Struct: use CToGo converter per element
					goElem := toPascalCase(t.Elem.Name)
					converter := toLowerCamelRaw(t.Elem.Name) + "CToGo"
					g.printf("\t\t%s: func() []%s {\n", goField, goElem)
					g.printf("\t\t\tn := int(%s)\n", lenExpr)
					g.printf("\t\t\tif n == 0 || %s == nil { return nil }\n", cExpr)
					g.printf("\t\t\tcSlice := unsafe.Slice(%s, n)\n", cExpr)
					g.printf("\t\t\tresult := make([]%s, n)\n", goElem)
					g.printf("\t\t\tfor i := 0; i < n; i++ { result[i] = %s(&cSlice[i]) }\n", converter)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				}
			}
		}
	case ast.TypeArray:
		if t.Elem != nil {
			arrLen := t.ArrayLen
			switch t.Elem.Kind {
			case ast.TypePrimitive:
				goElem := goTypeForDispatch(*t.Elem)
				g.printf("\t\t%s: func() [%d]%s {\n", goField, arrLen, goElem)
				g.printf("\t\t\tvar result [%d]%s\n", arrLen, goElem)
				g.printf("\t\t\tfor i := 0; i < %d; i++ { result[i] = %s(%s[i]) }\n", arrLen, goElem, cExpr)
				g.printf("\t\t\treturn result\n")
				g.printf("\t\t}(),\n")
			case ast.TypeNamed:
				if g.isEnum(t.Elem.Name) {
					goElem := toPascalCase(t.Elem.Name)
					g.printf("\t\t%s: func() [%d]%s {\n", goField, arrLen, goElem)
					g.printf("\t\t\tvar result [%d]%s\n", arrLen, goElem)
					g.printf("\t\t\tfor i := 0; i < %d; i++ { result[i] = %s(%s[i]) }\n", arrLen, goElem, cExpr)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				} else {
					goElem := toPascalCase(t.Elem.Name)
					converter := toLowerCamelRaw(t.Elem.Name) + "CToGo"
					g.printf("\t\t%s: func() [%d]%s {\n", goField, arrLen, goElem)
					g.printf("\t\t\tvar result [%d]%s\n", arrLen, goElem)
					g.printf("\t\t\tfor i := 0; i < %d; i++ { result[i] = %s(&%s[i]) }\n", arrLen, converter, cExpr)
					g.printf("\t\t\treturn result\n")
					g.printf("\t\t}(),\n")
				}
			}
		}
	}
}

func (g *dispatchCGen) isEnum(name string) bool {
	for _, e := range g.api.Enums {
		if e.Name == name {
			return true
		}
	}
	return false
}

// --- GoToC Converters ---
//
// All GoToC converters use a uniform signature with a freeList parameter
// for tracking C.CString allocations.  The caller (dispatch function)
// creates the freeList and defers freeing all pointers after the C call
// returns.  This ensures forward-compatibility: adding a string field
// to any struct type doesn't change converter signatures.

func (g *dispatchCGen) emitGoToCConverters() {
	// Only emit for struct types (not enums).
	hasStructs := false
	for _, t := range g.api.Types {
		if !g.isEnum(t.Name) {
			hasStructs = true
			break
		}
	}
	if !hasStructs {
		return
	}
	g.printf("// --- GoToC Converters ---\n\n")

	for _, t := range g.api.Types {
		if g.isEnum(t.Name) {
			continue
		}
		goName := toPascalCase(t.Name)
		cType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Name))
		funcName := toLowerCamelRaw(t.Name) + "GoToC"

		g.printf("func %s(src %s, freeList *[]unsafe.Pointer) %s {\n", funcName, goName, cType)
		g.printf("\tvar dst %s\n", cType)
		for _, f := range t.Fields {
			goField := toPascalCase(f.Name)
			cField := "dst." + cgoFieldAccess(f.Name)
			g.emitFieldGoToC(cField, "src."+goField, f.Type)
		}
		g.printf("\treturn dst\n")
		g.printf("}\n\n")
	}
}

// emitFieldGoToC emits an imperative assignment for a single struct field.
// String fields are allocated via C.CString and tracked in freeList.
func (g *dispatchCGen) emitFieldGoToC(cField, goExpr string, t ast.TypeRef) {
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case ast.PrimString:
			g.tmpSeq++
			tmp := fmt.Sprintf("_cs%d", g.tmpSeq)
			g.printf("\t%s := C.CString(%s)\n", tmp, goExpr)
			g.printf("\t*freeList = append(*freeList, unsafe.Pointer(%s))\n", tmp)
			g.printf("\t%s = %s\n", cField, tmp)
		case ast.PrimBool:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.bool(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.bool(%s)\n", cField, goExpr)
			}
		case ast.PrimI8:
			g.printf("\t%s = C.int8_t(%s)\n", cField, goExpr)
		case ast.PrimU8:
			g.printf("\t%s = C.uint8_t(%s)\n", cField, goExpr)
		case ast.PrimI16:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.int16_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.int16_t(%s)\n", cField, goExpr)
			}
		case ast.PrimU16:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.uint16_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.uint16_t(%s)\n", cField, goExpr)
			}
		case ast.PrimI32:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.int32_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.int32_t(%s)\n", cField, goExpr)
			}
		case ast.PrimU32:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.uint32_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.uint32_t(%s)\n", cField, goExpr)
			}
		case ast.PrimI64:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.int64_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.int64_t(%s)\n", cField, goExpr)
			}
		case ast.PrimU64:
			if t.Nullable {
				g.printf("\tif %s != nil { %s = C.uint64_t(*%s) }\n", goExpr, cField, goExpr)
			} else {
				g.printf("\t%s = C.uint64_t(%s)\n", cField, goExpr)
			}
		case ast.PrimF32:
			g.printf("\t%s = C.float(%s)\n", cField, goExpr)
		case ast.PrimF64:
			g.printf("\t%s = C.double(%s)\n", cField, goExpr)
		}
	case ast.TypeNamed:
		if g.isEnum(t.Name) {
			cType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Name))
			g.printf("\t%s = %s(%s)\n", cField, cType, goExpr)
		} else {
			converter := toLowerCamelRaw(t.Name) + "GoToC"
			g.printf("\t%s = %s(%s, freeList)\n", cField, converter, goExpr)
		}
	case ast.TypeSlice:
		// Slice field: allocate C array, convert elements, set pointer + length.
		g.tmpSeq++
		nVar := fmt.Sprintf("_n%d", g.tmpSeq)
		arrVar := fmt.Sprintf("_arr%d", g.tmpSeq)
		g.printf("\t%s := len(%s)\n", nVar, goExpr)
		g.printf("\tif %s > 0 {\n", nVar)
		lenField := cField + "_len"
		g.printf("\t\t%s = C.size_t(%s)\n", lenField, nVar)
		if t.Elem.Kind == ast.TypePrimitive && t.Elem.Name == ast.PrimString {
			// []string → allocate **C.char array
			g.printf("\t\t%s := (**C.char)(C.calloc(C.size_t(%s), C.size_t(unsafe.Sizeof((*C.char)(nil)))))\n", arrVar, nVar)
			g.printf("\t\t*freeList = append(*freeList, unsafe.Pointer(%s))\n", arrVar)
			g.printf("\t\t_sl%d := unsafe.Slice(%s, %s)\n", g.tmpSeq, arrVar, nVar)
			g.printf("\t\tfor _i%d := range %s {\n", g.tmpSeq, goExpr)
			g.printf("\t\t\t_sl%d[_i%d] = C.CString(%s[_i%d])\n", g.tmpSeq, g.tmpSeq, goExpr, g.tmpSeq)
			g.printf("\t\t\t*freeList = append(*freeList, unsafe.Pointer(_sl%d[_i%d]))\n", g.tmpSeq, g.tmpSeq)
			g.printf("\t\t}\n")
			g.printf("\t\t%s = %s\n", cField, arrVar)
		} else if t.Elem.Kind == ast.TypeNamed && !g.isEnum(t.Elem.Name) {
			// []Struct → allocate array of C structs
			elemCType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Elem.Name))
			converter := toLowerCamelRaw(t.Elem.Name) + "GoToC"
			g.printf("\t\t%s := (*%s)(C.calloc(C.size_t(%s), C.size_t(unsafe.Sizeof(%s{}))))\n", arrVar, elemCType, nVar, elemCType)
			g.printf("\t\t*freeList = append(*freeList, unsafe.Pointer(%s))\n", arrVar)
			g.printf("\t\t_sl%d := unsafe.Slice(%s, %s)\n", g.tmpSeq, arrVar, nVar)
			g.printf("\t\tfor _i%d := range %s {\n", g.tmpSeq, goExpr)
			g.printf("\t\t\t_sl%d[_i%d] = %s(%s[_i%d], freeList)\n", g.tmpSeq, g.tmpSeq, converter, goExpr, g.tmpSeq)
			g.printf("\t\t}\n")
			g.printf("\t\t%s = %s\n", cField, arrVar)
		} else {
			// []primitive (non-string) → allocate array of C type
			elemCType := cTypeForAPICgo(g.api.Name, *t.Elem)
			// Use appropriate zero value for Sizeof (bool needs special handling)
			zeroVal := elemCType + "(0)"
			if t.Elem.Name == ast.PrimBool {
				zeroVal = elemCType + "(false)"
			}
			g.printf("\t\t%s := (*%s)(C.calloc(C.size_t(%s), C.size_t(unsafe.Sizeof(%s))))\n", arrVar, elemCType, nVar, zeroVal)
			g.printf("\t\t*freeList = append(*freeList, unsafe.Pointer(%s))\n", arrVar)
			g.printf("\t\t_sl%d := unsafe.Slice(%s, %s)\n", g.tmpSeq, arrVar, nVar)
			g.printf("\t\tfor _i%d := range %s {\n", g.tmpSeq, goExpr)
			g.printf("\t\t\t_sl%d[_i%d] = %s(%s[_i%d])\n", g.tmpSeq, g.tmpSeq, elemCType, goExpr, g.tmpSeq)
			g.printf("\t\t}\n")
			g.printf("\t\t%s = %s\n", cField, arrVar)
		}
		g.printf("\t}\n")
	case ast.TypeArray:
		if t.Elem != nil {
			arrLen := t.ArrayLen
			switch t.Elem.Kind {
			case ast.TypePrimitive:
				elemCType := cTypeForAPICgo(g.api.Name, *t.Elem)
				g.printf("\tfor _i := 0; _i < %d; _i++ { %s[_i] = %s(%s[_i]) }\n", arrLen, cField, elemCType, goExpr)
			case ast.TypeNamed:
				if g.isEnum(t.Elem.Name) {
					elemCType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Elem.Name))
					g.printf("\tfor _i := 0; _i < %d; _i++ { %s[_i] = %s(%s[_i]) }\n", arrLen, cField, elemCType, goExpr)
				} else {
					converter := toLowerCamelRaw(t.Elem.Name) + "GoToC"
					g.printf("\tfor _i := 0; _i < %d; _i++ { %s[_i] = %s(%s[_i], freeList) }\n", arrLen, cField, converter, goExpr)
				}
			}
		}
	}
}

// --- Dispatch Functions ---

func (g *dispatchCGen) emitDispatchFuncs() {
	if len(g.api.Funcs) == 0 {
		return
	}
	g.printf("// --- Dispatch Functions ---\n\n")

	for _, fn := range g.api.Funcs {
		if fn.Publish {
			continue
		}
		g.emitOneDispatch(fn)
	}
}

func (g *dispatchCGen) emitOneDispatch(fn ast.Func) {
	apiName := g.api.Name
	fnSnake := toSnakeCase(fn.Name)
	dispatchName := apiName + "Dispatch" + toPascalCase(fn.Name)
	wrapperName := fmt.Sprintf("C.call_%s_%s", apiName, fnSnake)
	cbsType := fmt.Sprintf("C.%s_callbacks_t", apiName)

	g.printf("func %s(callbacks unsafe.Pointer, req []byte) ([]byte, error) {\n", dispatchName)
	g.printf("\tcb := (*%s)(callbacks)\n", cbsType)
	g.printf("\tvar _freeList []unsafe.Pointer\n")
	g.printf("\tdefer func() { for _, p := range _freeList { C.free(p) } }()\n")

	// Unmarshal JSON params
	if len(fn.Params) > 0 {
		g.printf("\tvar params struct {\n")
		for _, p := range fn.Params {
			fieldName := toPascalCase(p.Name)
			var fieldType string
			if p.IsPtr {
				fieldType = "uint64"
			} else {
				fieldType = goTypeForDispatch(p.Type)
			}
			jsonTag := p.Name
			g.printf("\t\t%s %s `json:\"%s\"`\n", fieldName, fieldType, jsonTag)
		}
		g.printf("\t}\n")
		g.printf("\tif len(req) > 0 {\n")
		g.printf("\t\tif err := json.Unmarshal(req, &params); err != nil {\n")
		g.printf("\t\t\treturn nil, syscall.EINVAL\n")
		g.printf("\t\t}\n")
		g.printf("\t}\n")

		// Validate IDL @constraints before converting/dispatching.
		g.printf("%s", g.ce.validation(fn))
	}

	// Convert Go params → C and build call args
	var callArgs []string
	callArgs = append(callArgs, "cb."+cgoFieldAccess(fnSnake))
	callArgs = append(callArgs, "cb.ctx")

	for _, p := range fn.Params {
		cVar := "c" + toPascalCase(p.Name)
		goVar := "params." + toPascalCase(p.Name)
		g.emitParamGoToC(cVar, goVar, p)
		callArgs = append(callArgs, g.paramCallArg(cVar, p)...)
	}

	// Call C wrapper — direct return (no error code, no out-param)
	if fn.Return != nil {
		g.printf("\tout := %s(%s)\n", wrapperName, strings.Join(callArgs, ", "))
		g.emitReturnConvert(fn)
	} else {
		g.printf("\t%s(%s)\n", wrapperName, strings.Join(callArgs, ", "))
		g.printf("\treturn nil, nil\n")
	}

	g.printf("}\n\n")
}

func (g *dispatchCGen) emitParamGoToC(cVar, goVar string, p ast.Param) {
	// ptr qualifier: cast uintptr → unsafe.Pointer → *C.type (no marshaling).
	if p.IsPtr {
		var cType string
		switch p.Type.Kind {
		case ast.TypePrimitive:
			cType = "*C." + primitiveToCType(p.Type.Name)
		default:
			cType = fmt.Sprintf("*C.%s_%s_t", g.api.Name, toSnakeCase(p.Type.Name))
		}
		g.printf("\t%s := (%s)(unsafe.Pointer(uintptr(%s)))\n", cVar, cType, goVar)
		return
	}
	t := p.Type
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case ast.PrimString:
			g.printf("\t%s := C.CString(%s)\n", cVar, goVar)
			g.printf("\t_freeList = append(_freeList, unsafe.Pointer(%s))\n", cVar)
		case ast.PrimBool:
			if t.Nullable {
				g.printf("\tvar %s C.bool\n", cVar)
				g.printf("\tif %s != nil { %s = C.bool(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.bool(%s)\n", cVar, goVar)
			}
		case ast.PrimI8:
			g.printf("\t%s := C.int8_t(%s)\n", cVar, goVar)
		case ast.PrimU8:
			g.printf("\t%s := C.uint8_t(%s)\n", cVar, goVar)
		case ast.PrimI16:
			if t.Nullable {
				g.printf("\tvar %s C.int16_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.int16_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.int16_t(%s)\n", cVar, goVar)
			}
		case ast.PrimU16:
			if t.Nullable {
				g.printf("\tvar %s C.uint16_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.uint16_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.uint16_t(%s)\n", cVar, goVar)
			}
		case ast.PrimI32:
			if t.Nullable {
				g.printf("\tvar %s C.int32_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.int32_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.int32_t(%s)\n", cVar, goVar)
			}
		case ast.PrimU32:
			if t.Nullable {
				g.printf("\tvar %s C.uint32_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.uint32_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.uint32_t(%s)\n", cVar, goVar)
			}
		case ast.PrimI64:
			if t.Nullable {
				g.printf("\tvar %s C.int64_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.int64_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.int64_t(%s)\n", cVar, goVar)
			}
		case ast.PrimU64:
			if t.Nullable {
				g.printf("\tvar %s C.uint64_t\n", cVar)
				g.printf("\tif %s != nil { %s = C.uint64_t(*%s) }\n", goVar, cVar, goVar)
			} else {
				g.printf("\t%s := C.uint64_t(%s)\n", cVar, goVar)
			}
		case ast.PrimF32:
			g.printf("\t%s := C.float(%s)\n", cVar, goVar)
		case ast.PrimF64:
			g.printf("\t%s := C.double(%s)\n", cVar, goVar)
		case ast.PrimPtr:
			// ptr is an opaque pointer — not serializable; pass nil for non-REST stubs
			g.printf("\tvar %s unsafe.Pointer // ptr: not serializable\n", cVar)
		}
	case ast.TypeCallback:
		// Callback function pointers cannot be serialized; pass nil for non-REST stubs
		g.printf("\tvar %s unsafe.Pointer // callback: not serializable\n", cVar)
	case ast.TypeImport:
		// Imported struct pointers cannot be serialized; pass nil for non-REST stubs
		g.printf("\tvar %s unsafe.Pointer // import: not serializable\n", cVar)
	case ast.TypeNamed:
		if g.isEnum(t.Name) {
			cType := fmt.Sprintf("C.%s_%s_t", g.api.Name, toSnakeCase(t.Name))
			g.printf("\t%s := %s(%s)\n", cVar, cType, goVar)
		} else {
			converter := toLowerCamelRaw(t.Name) + "GoToC"
			g.printf("\t%s := %s(%s, &_freeList)\n", cVar, converter, goVar)
		}
	case ast.TypeArray:
		// Convert Go [N]T → C [N]T element by element.
		elemCType := cTypeForAPICgo(g.api.Name, *t.Elem)
		g.printf("\tvar %s [%d]%s\n", cVar, t.ArrayLen, elemCType)
		g.printf("\tfor i := 0; i < %d; i++ {\n", t.ArrayLen)
		g.printf("\t\t%s[i] = %s(%s[i])\n", cVar, elemCType, goVar)
		g.printf("\t}\n")
	case ast.TypeSlice:
		if t.Elem != nil && t.Elem.Kind == ast.TypePrimitive && t.Elem.Name == ast.PrimString {
			// []string → char** + count
			g.printf("\t%sLen := C.size_t(len(%s))\n", cVar, goVar)
			g.printf("\t%s := (**C.char)(C.malloc(C.size_t(len(%s)) * C.size_t(unsafe.Sizeof((*C.char)(nil)))))\n", cVar, goVar)
			g.printf("\tdefer C.free(unsafe.Pointer(%s))\n", cVar)
			g.printf("\t%sSlice := unsafe.Slice(%s, len(%s))\n", cVar, cVar, goVar)
			g.printf("\tfor i, s := range %s {\n", goVar)
			g.printf("\t\t%sSlice[i] = C.CString(s)\n", cVar)
			g.printf("\t}\n")
			g.printf("\tdefer func() {\n")
			g.printf("\t\tfor i := range %s {\n", goVar)
			g.printf("\t\t\tC.free(unsafe.Pointer(%sSlice[i]))\n", cVar)
			g.printf("\t\t}\n")
			g.printf("\t}()\n")
		} else if t.Elem != nil && t.Elem.Kind == ast.TypeNamed {
			// []Struct → C array of struct_t + length
			elemCType := cTypeForAPICgo(g.api.Name, *t.Elem)
			converter := toLowerCamelRaw(t.Elem.Name) + "GoToC"
			g.printf("\t%sLen := C.size_t(len(%s))\n", cVar, goVar)
			g.printf("\tvar %s *%s\n", cVar, elemCType)
			g.printf("\tif len(%s) > 0 {\n", goVar)
			g.printf("\t\t%s = (*%s)(C.malloc(C.size_t(len(%s)) * C.size_t(unsafe.Sizeof(%s{}))))\n", cVar, elemCType, goVar, elemCType)
			g.printf("\t\t_freeList = append(_freeList, unsafe.Pointer(%s))\n", cVar)
			g.printf("\t\t%sSlice := unsafe.Slice(%s, len(%s))\n", cVar, cVar, goVar)
			g.printf("\t\tfor i, v := range %s {\n", goVar)
			g.printf("\t\t\t%sSlice[i] = %s(v, &_freeList)\n", cVar, converter)
			g.printf("\t\t}\n")
			g.printf("\t}\n")
		} else if t.Elem != nil {
			// []primitive → C array + length
			elemCType := cTypeForAPICgo(g.api.Name, *t.Elem)
			g.printf("\t%sLen := C.size_t(len(%s))\n", cVar, goVar)
			g.printf("\tvar %s *%s\n", cVar, elemCType)
			g.printf("\tif len(%s) > 0 {\n", goVar)
			g.printf("\t\t%s = (*%s)(C.malloc(C.size_t(len(%s)) * C.size_t(unsafe.Sizeof(%s(0)))))\n", cVar, elemCType, goVar, elemCType)
			g.printf("\t\t_freeList = append(_freeList, unsafe.Pointer(%s))\n", cVar)
			g.printf("\t\t%sSlice := unsafe.Slice(%s, len(%s))\n", cVar, cVar, goVar)
			g.printf("\t\tfor i, v := range %s {\n", goVar)
			g.printf("\t\t\t%sSlice[i] = %s(v)\n", cVar, elemCType)
			g.printf("\t\t}\n")
			g.printf("\t}\n")
		}
	}
}

// paramCallArg returns the C call arguments for a parameter.
// Handles byref/out (pass pointer), arrays (pass pointer to first element), etc.
func (g *dispatchCGen) paramCallArg(cVar string, p ast.Param) []string {
	// ptr qualifier: cVar is already a pointer, pass directly.
	if p.IsPtr {
		return []string{cVar}
	}
	switch p.Type.Kind {
	case ast.TypePrimitive:
		if p.ByRef || p.IsOut {
			return []string{"&" + cVar}
		}
		return []string{cVar}
	case ast.TypeNamed:
		// Enums pass by value; structs pass by pointer (const or mutable).
		if g.isEnum(p.Type.Name) {
			if p.ByRef || p.IsOut {
				return []string{"&" + cVar}
			}
			return []string{cVar}
		}
		return []string{"&" + cVar}
	case ast.TypeArray:
		// Arrays pass pointer to first element.
		return []string{"&" + cVar + "[0]"}
	case ast.TypeSlice:
		return []string{cVar, cVar + "Len"}
	}
	return []string{cVar}
}

// emitReturnSetup is unused — kept as placeholder for slice-return APIs.
func (g *dispatchCGen) emitReturnSetup(fn ast.Func) {
}

func (g *dispatchCGen) emitReturnConvert(fn ast.Func) {
	ret := fn.Return

	switch ret.Kind {
	case ast.TypePrimitive:
		switch ret.Name {
		case ast.PrimString:
			g.printf("\tresult := C.GoString(out)\n")
		case ast.PrimBool:
			g.printf("\tresult := bool(out)\n")
		default:
			goType := primitiveToGoType(ret.Name)
			g.printf("\tresult := %s(out)\n", goType)
		}
		g.printf("\treturn json.Marshal(result)\n")

	case ast.TypeNamed:
		if g.isEnum(ret.Name) {
			goType := toPascalCase(ret.Name)
			g.printf("\tresult := %s(out)\n", goType)
		} else {
			converter := toLowerCamelRaw(ret.Name) + "CToGo"
			if g.typeHasSlices(ret.Name) {
				converter += "Free"
			}
			g.printf("\tresult := %s(&out)\n", converter)
		}
		g.printf("\treturn json.Marshal(result)\n")

	case ast.TypeSlice:
		// out is a result struct with .data (pointer) and .len (size_t)
		g.printf("\tn := int(out.len)\n")
		if ret.Elem.Kind == ast.TypeNamed && !g.isEnum(ret.Elem.Name) {
			goElemType := toPascalCase(ret.Elem.Name)
			converter := toLowerCamelRaw(ret.Elem.Name) + "CToGo"
			g.printf("\tresult := make([]%s, n)\n", goElemType)
			g.printf("\tif n > 0 {\n")
			g.printf("\t\tcSlice := unsafe.Slice(out.data, n)\n")
			g.printf("\t\tfor i := 0; i < n; i++ {\n")
			g.printf("\t\t\tresult[i] = %s(&cSlice[i])\n", converter)
			g.printf("\t\t}\n")
			g.printf("\t\tC.free(unsafe.Pointer(out.data))\n")
			g.printf("\t}\n")
		} else {
			goElemType := goTypeForDispatch(*ret.Elem)
			g.printf("\tresult := make([]%s, n)\n", goElemType)
			g.printf("\tif n > 0 {\n")
			g.printf("\t\tcSlice := unsafe.Slice(out.data, n)\n")
			g.printf("\t\tfor i := 0; i < n; i++ {\n")
			if ret.Elem.Kind == ast.TypePrimitive && ret.Elem.Name == ast.PrimString {
				g.printf("\t\t\tresult[i] = C.GoString(cSlice[i])\n")
			} else {
				g.printf("\t\t\tresult[i] = %s(cSlice[i])\n", goElemType)
			}
			g.printf("\t\t}\n")
			g.printf("\t\tC.free(unsafe.Pointer(out.data))\n")
			g.printf("\t}\n")
		}
		// For []u8 (byte slices), return raw bytes without JSON encoding.
		// This supports BinaryWatchFunc which sends data as binary WS frames.
		if ret.Elem.Kind == ast.TypePrimitive && ret.Elem.Name == "u8" {
			g.printf("\treturn result, nil\n")
		} else {
			g.printf("\treturn json.Marshal(result)\n")
		}
	}
}

// --- APIMeta ---

func (g *dispatchCGen) emitMeta() {
	metaVar := toPascalCase(g.api.Name) + "Meta"
	g.printf("// --- API Metadata ---\n\n")
	g.printf("var %s = &apiserver.APIMeta{\n", metaVar)
	g.printf("\tName:       %q,\n", g.api.Name)
	g.printf("\tVersion:    %d,\n", g.api.Version)
	g.printf("\tRESTExport: %t,\n", g.api.RestExport)
	g.printf("\tPrefix:     %q,\n", g.api.Prefix)
	g.printf("\tFuncs: []apiserver.FuncMeta{\n")
	for _, fn := range g.api.Funcs {
		if fn.Publish {
			continue
		}
		dispatchName := g.api.Name + "Dispatch" + toPascalCase(fn.Name)
		g.printf("\t\t{\n")
		g.printf("\t\t\tName:     %q,\n", fn.Name)
		g.printf("\t\t\tMethod:   %q,\n", fn.Method)
		g.printf("\t\t\tPath:     %q,\n", fn.Path)
		g.printf("\t\t\tRTSafe:   %t,\n", fn.RTSafe)
		g.printf("\t\t\tDispatch: %s,\n", dispatchName)
		g.printf("\t\t},\n")
	}
	g.printf("\t},\n")
	g.printf("}\n\n")
}

