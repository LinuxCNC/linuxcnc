// --- Type Helpers ---

// toCTypeForAPI returns the C type string for use in the cgo preamble.
func toCTypeForAPI(apiName string, t ast.TypeRef) string {
	if t.Nullable && t.Kind == ast.TypePrimitive && t.Name == ast.PrimString {
		return "const char *"
	}
	switch t.Kind {
	case ast.TypePrimitive:
		return primitiveToCType(t.Name)
	case ast.TypeNamed:
		return fmt.Sprintf("%s_%s_t", apiName, toSnakeCase(t.Name))
	case ast.TypeSlice:
		elemType := toCTypeForAPI(apiName, *t.Elem)
		return fmt.Sprintf("%s *", elemType)
	case ast.TypeArray:
		elemType := toCTypeForAPI(apiName, *t.Elem)
		return fmt.Sprintf("%s[%d]", elemType, t.ArrayLen)
	}
	return "void"
}

// cTypeForAPICgo returns the C type for use in Go code (C. prefix).
func cTypeForAPICgo(apiName string, t ast.TypeRef) string {
	switch t.Kind {
	case ast.TypePrimitive:
		switch t.Name {
		case ast.PrimBool:
			return "C.bool"
		case ast.PrimI8:
			return "C.int8_t"
		case ast.PrimU8:
			return "C.uint8_t"
		case ast.PrimI16:
			return "C.int16_t"
		case ast.PrimU16:
			return "C.uint16_t"
		case ast.PrimI32:
			return "C.int32_t"
		case ast.PrimU32:
			return "C.uint32_t"
		case ast.PrimI64:
			return "C.int64_t"
		case ast.PrimU64:
			return "C.uint64_t"
		case ast.PrimF32:
			return "C.float"
		case ast.PrimF64:
			return "C.double"
		case ast.PrimString:
			return "*C.char"
		}
	case ast.TypeNamed:
		return fmt.Sprintf("C.%s_%s_t", apiName, toSnakeCase(t.Name))
	case ast.TypeSlice:
		return cTypeForAPICgo(apiName, *t.Elem)
	}
	return "C.int"
}

// goTypeForDispatch returns Go type string for use in dispatch code.
func goTypeForDispatch(t ast.TypeRef) string {
	switch t.Kind {
	case ast.TypePrimitive:
		base := primitiveToGoType(t.Name)
		if t.Nullable && t.Name != ast.PrimString {
			return "*" + base
		}
		return base
	case ast.TypeNamed:
		name := toPascalCase(t.Name)
		if t.Nullable {
			return "*" + name
		}
		return name
	case ast.TypeSlice:
		return "[]" + goTypeForDispatch(*t.Elem)
	case ast.TypeArray:
		return fmt.Sprintf("[%d]%s", t.ArrayLen, goTypeForDispatch(*t.Elem))
	}
	return "interface{}"
}

// cgoFieldAccess returns the field name for accessing a C struct field from Go.
// Go keywords get a _ prefix in cgo.
func cgoFieldAccess(cFieldName string) string {
	// C keywords get underscore suffix in struct fields (see cSafeName).
	// cgo accesses those escaped names directly.
	if cKeywords[cFieldName] {
		return cFieldName + "_"
	}
	if goKeywords[cFieldName] {
		return "_" + cFieldName
	}
	return cFieldName
}

// toLowerCamelRaw converts snake_case to lowerCamelCase without keyword escaping.
func toLowerCamelRaw(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if i > 0 && len(p) > 0 {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	result := strings.Join(parts, "")
	if len(result) > 0 {
		result = strings.ToLower(result[:1]) + result[1:]
	}
	return result
}
