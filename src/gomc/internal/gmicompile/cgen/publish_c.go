package cgen

import (
	"fmt"
	"io"
	"strings"

	"github.com/sittner/linuxcnc/src/gomc/internal/gmicompile/ast"
)

// GeneratePublishHeader writes a C header with ring buffer types and RT-safe
// publish functions for all @publish functions in the API.
// Returns false if the API has no @publish functions (nothing to generate).
func GeneratePublishHeader(w io.Writer, api *ast.API) (bool, error) {
	g := &publishCGen{w: w, api: api}
	if !g.hasPublishFuncs() {
		return false, nil
	}
	return true, g.generate()
}

type publishCGen struct {
	w   io.Writer
	api *ast.API
	err error
}

func (g *publishCGen) printf(format string, args ...interface{}) {
	if g.err != nil {
		return
	}
	_, g.err = fmt.Fprintf(g.w, format, args...)
}

func (g *publishCGen) hasPublishFuncs() bool {
	for _, fn := range g.api.Funcs {
		if fn.Publish {
			return true
		}
	}
	return false
}

func (g *publishCGen) generate() error {
	prefix := g.api.Name
	guard := strings.ToUpper(prefix) + "_PUB_H"

	g.printf("// Generated by gmicompile - DO NOT EDIT\n")
	g.printf("#ifndef %s\n", guard)
	g.printf("#define %s\n\n", guard)
	g.printf("#include <stdint.h>\n")
	g.printf("#include <stdarg.h>\n")
	g.printf("#include <string.h>\n")
	g.printf("#include <stdlib.h>\n")
	g.printf("#ifndef %s_API_CGO\n", strings.ToUpper(prefix))
	g.printf("#define %s_API_CGO\n", strings.ToUpper(prefix))
	g.printf("#endif\n")
	g.printf("#include \"%s_api.h\"\n\n", g.api.Name)
	g.printf("#ifdef __cplusplus\nextern \"C\" {\n#endif\n\n")

	for _, fn := range g.api.Funcs {
		if !fn.Publish {
			continue
		}
		g.emitSlotStruct(fn)
		g.emitRingStruct(fn)
		g.emitPublishFunc(fn)
		g.emitRingCreate(fn)
	}

	g.printf("#ifdef __cplusplus\n}\n#endif\n\n")
	g.printf("#endif // %s\n", guard)
	return g.err
}

// ringSize returns the ring size for a publish function (default 64).
func (g *publishCGen) ringSize(fn ast.Func) int {
	if fn.PublishRingSize > 0 {
		return fn.PublishRingSize
	}
	return 64
}

// slotTypeName returns e.g. "emcerror_publish_error_slot_t"
func (g *publishCGen) slotTypeName(fn ast.Func) string {
	return fmt.Sprintf("%s_%s_slot_t", g.api.Name, fn.Name)
}

// ringTypeName returns e.g. "emcerror_publish_error_ring_t"
func (g *publishCGen) ringTypeName(fn ast.Func) string {
	return fmt.Sprintf("%s_%s_ring_t", g.api.Name, fn.Name)
}

func (g *publishCGen) emitSlotStruct(fn ast.Func) {
	slotType := g.slotTypeName(fn)
	g.printf("// --- %s slot ---\n\n", fn.Name)
	g.printf("typedef struct {\n")
	g.printf("    uint32_t seq;  // 0 = free, pos+1 = ready\n")

	for _, p := range fn.Params {
		cType := publishParamCType(g.api, p)
		if p.Type.Kind == ast.TypePrimitive && p.Type.Name == "string" {
			// Strings stored as fixed-size char arrays in slot.
			g.printf("    char %s[256];\n", p.Name)
		} else {
			g.printf("    %s %s;\n", cType, p.Name)
		}
	}

	g.printf("} %s;\n\n", slotType)
}

func (g *publishCGen) emitRingStruct(fn ast.Func) {
	ringType := g.ringTypeName(fn)
	slotType := g.slotTypeName(fn)
	size := g.ringSize(fn)
	prefix := strings.ToUpper(g.api.Name) + "_" + strings.ToUpper(fn.Name)

	g.printf("#define %s_RING_SIZE %d\n", prefix, size)
	g.printf("#define %s_RING_MASK (%d)\n\n", prefix, size-1)

	g.printf("typedef struct {\n")
	g.printf("    uint32_t write_pos;\n")
	g.printf("    char _pad[60];\n")
	g.printf("    uint32_t read_pos;\n")
	g.printf("    %s slots[%s_RING_SIZE];\n", slotType, prefix)
	g.printf("} %s;\n\n", ringType)
}

func (g *publishCGen) emitPublishFunc(fn ast.Func) {
	ringType := g.ringTypeName(fn)
	slotType := g.slotTypeName(fn)
	prefix := strings.ToUpper(g.api.Name) + "_" + strings.ToUpper(fn.Name)
	funcName := fmt.Sprintf("%s_%s", g.api.Name, fn.Name)

	// Function signature
	g.printf("static inline int\n%s(%s *ring", funcName, ringType)
	for _, p := range fn.Params {
		cType := publishParamCType(g.api, p)
		g.printf(", %s %s", cType, p.Name)
	}
	g.printf(") {\n")

	// Claim slot
	g.printf("    uint32_t pos = __atomic_fetch_add(&ring->write_pos, 1, __ATOMIC_RELAXED);\n")
	g.printf("    uint32_t idx = pos & %s_RING_MASK;\n", prefix)
	g.printf("    %s *slot = &ring->slots[idx];\n\n", slotType)

	// CAS to acquire
	g.printf("    uint32_t expected = 0;\n")
	g.printf("    if (!__atomic_compare_exchange_n(&slot->seq, &expected, 1, 0,\n")
	g.printf("                                     __ATOMIC_ACQUIRE, __ATOMIC_RELAXED)) {\n")
	g.printf("        return -1;  // ring full\n")
	g.printf("    }\n\n")

	// Fill fields
	for _, p := range fn.Params {
		if p.Type.Kind == ast.TypePrimitive && p.Type.Name == "string" {
			g.printf("    strncpy(slot->%s, %s, sizeof(slot->%s) - 1);\n", p.Name, p.Name, p.Name)
			g.printf("    slot->%s[sizeof(slot->%s) - 1] = '\\0';\n", p.Name, p.Name)
		} else {
			g.printf("    slot->%s = %s;\n", p.Name, p.Name)
		}
	}

	// Publish
	g.printf("\n    __atomic_store_n(&slot->seq, pos + 1, __ATOMIC_RELEASE);\n")
	g.printf("    return 0;\n")
	g.printf("}\n\n")
}

func (g *publishCGen) emitRingCreate(fn ast.Func) {
	ringType := g.ringTypeName(fn)
	funcName := fmt.Sprintf("%s_%s_ring_create", g.api.Name, fn.Name)
	destroyName := fmt.Sprintf("%s_%s_ring_destroy", g.api.Name, fn.Name)

	g.printf("static inline %s *%s(void) {\n", ringType, funcName)
	g.printf("    return (%s *)calloc(1, sizeof(%s));\n", ringType, ringType)
	g.printf("}\n\n")

	g.printf("static inline void %s(%s *r) {\n", destroyName, ringType)
	g.printf("    free(r);\n")
	g.printf("}\n\n")

	// Emit ring_init helper: allocates ring + registers it via gomc_api_t.
	// The cmod calls this in New() to own the ring.
	initName := fmt.Sprintf("%s_%s_ring_init", g.api.Name, fn.Name)
	apiRegName := fmt.Sprintf("%s_%s", g.api.Name, fn.Name)
	g.printf("#ifdef GOMC_API_H\n")
	g.printf("static inline %s *%s(const gomc_api_t *api, const char *instance) {\n", ringType, initName)
	g.printf("    %s *ring = %s();\n", ringType, funcName)
	g.printf("    if (!ring) return NULL;\n")
	g.printf("    if (api->register_api(api->ctx, \"%s\", 1, instance, (void *)ring) != 0) {\n", apiRegName)
	g.printf("        free(ring);\n")
	g.printf("        return NULL;\n")
	g.printf("    }\n")
	g.printf("    return ring;\n")
	g.printf("}\n")
	g.printf("#endif\n\n")
}

// publishParamCType maps a Param type to its C representation for the publish function signature.
func publishParamCType(api *ast.API, p ast.Param) string {
	t := p.Type
	switch t.Kind {
	case ast.TypePrimitive:
		if t.Name == "string" {
			return "const char *"
		}
		return primitiveToCType(t.Name)
	case ast.TypeNamed:
		// Check if it's an enum
		for _, e := range api.Enums {
			if e.Name == t.Name {
				return fmt.Sprintf("%s_%s_t", api.Name, toSnakeCase(t.Name))
			}
		}
		return fmt.Sprintf("%s_%s_t", api.Name, toSnakeCase(t.Name))
	}
	return "int"
}
